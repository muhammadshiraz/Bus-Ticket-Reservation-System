    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String bname = "'" + txtbname.getText() + "'";
        String typecombo = "'" + cmbtype.getSelectedItem().toString() + "'";
        String bcap = "'" + txtbcap.getText() + "'";
        String bnum = "'" + txtbnum.getText() + "'";
        String bprice = "'" + txtbprice.getText() + "'";
        
        
        if(txtbname.getText().equals("") || cmbtype.getSelectedIndex()==0 || txtbcap.getText().equals("")
                || txtbnum.getText().equals("") || txtbprice.getText().equals(""))
        {
                JOptionPane.showMessageDialog(this, "You Forgot To Fill All The Fields");
                new MakeSound().playSound("src//img//error1.wav");
        
        }
        else
        {
                try
                {

                st.executeUpdate("insert into TB_Buses values (" + bname  +"," +typecombo+"," +bcap+"," +bnum+""
                         + "," +bprice+")");
                 
                JOptionPane.showMessageDialog(this, "Bus Added");
                clearfields();
                


                }catch(SQLException | HeadlessException e)
                {   
                    JOptionPane.showMessageDialog(this, "Check All the Fields Again");
                }
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

